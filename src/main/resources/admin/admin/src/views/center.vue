// 校验引入
import { 
	isIntNumer,
	isMobile,
} from "@/utils/validate";

export default {
	data() {
		return {
			ruleForm: {},
			flag: '',
			usersFlag: false,
			waimaiyuanxingbieOptions: [],
		};
	},
	mounted() {
		var table = this.$storage.get("sessionTable");
		this.flag = table;
		this.$http({
			url: `${this.$storage.get("sessionTable")}/session`,
			method: "get"
		}).then(({ data }) => {
			if (data && data.code === 0) {
				this.ruleForm = data.data;
			} else {
				this.$message.error(data.msg);
			}
		});
		this.waimaiyuanxingbieOptions = "男,女".split(',')
	},
	methods: {
		waimaiyuantouxiangUploadChange(fileUrls) {
			this.ruleForm.touxiang = fileUrls;
		},
		xiaoquguanliyuantouxiangUploadChange(fileUrls) {
			this.ruleForm.touxiang = fileUrls;
		},
		usersimageUploadChange(fileUrls) {
			this.ruleForm.image = fileUrls;
		},
		onUpdateHandler() {
			if((!this.ruleForm.waimaizhanghao)&& 'waimaiyuan'==this.flag){
				this.$message.error('外卖账号不能为空');
				return
			}


			if((!this.ruleForm.mima)&& 'waimaiyuan'==this.flag){
				this.$message.error('密码不能为空');
				return
			}

			if((!this.ruleForm.waimaixingming)&& 'waimaiyuan'==this.flag){
				this.$message.error('外卖姓名不能为空');
				return
			}




			if( 'waimaiyuan' ==this.flag && this.ruleForm.nianling&&(!isIntNumer(this.ruleForm.nianling))){
				this.$message.error(`年龄应输入整数`);
				return
			}


			if((!this.ruleForm.lianxidianhua)&& 'waimaiyuan'==this.flag){
				this.$message.error('联系电话不能为空');
				return
			}


			if( 'waimaiyuan' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
				this.$message.error(`联系电话应输入手机格式`);
				return
			}
			if((!this.ruleForm.touxiang)&& 'waimaiyuan'==this.flag){
				this.$message.error('头像不能为空');
				return
			}


			if(this.ruleForm.touxiang!=null) {
				this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
			}
			if((!this.ruleForm.peisongpingtai)&& 'waimaiyuan'==this.flag){
				this.$message.error('配送平台不能为空');
				return
			}




			if( 'waimaiyuan' ==this.flag && this.ruleForm.xinyuqingkuang&&(!isIntNumer(this.ruleForm.xinyuqingkuang))){
				this.$message.error(`信誉情况应输入整数`);
				return
			}
			if((!this.ruleForm.guanlizhanghao)&& 'xiaoquguanliyuan'==this.flag){
				this.$message.error('管理账号不能为空');
				return
			}


			if((!this.ruleForm.mima)&& 'xiaoquguanliyuan'==this.flag){
				this.$message.error('密码不能为空');
				return
			}





			if(this.ruleForm.touxiang!=null) {
				this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
			}


			if( 'xiaoquguanliyuan' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
				this.$message.error(`联系电话应输入手机格式`);
				return
			}
			if((!this.ruleForm.xiaoqumingcheng)&& 'xiaoquguanliyuan'==this.flag){
				this.$message.error('小区名称不能为空');
				return
			}


			if((!this.ruleForm.xiaoqudizhi)&& 'xiaoquguanliyuan'==this.flag){
				this.$message.error('小区地址不能为空');
				return
			}


			if('users'==this.flag && this.ruleForm.username.trim().length<1) {
				this.$message.error(`用户名不能为空`);
				return	
			}
			if(this.flag=='users'){
				this.ruleForm.image = this.ruleForm.image.replace(new RegExp(this.$base.url,"g"),"")
			}
			this.$http({
				url: `${this.$storage.get("sessionTable")}/update`,
				method: "post",
				data: this.ruleForm
			}).then(({ data }) => {
				if (data && data.code === 0) {
					this.$message({
						message: "修改信息成功",
						type: "success",
						duration: 1500,
						onClose: () => {
							if(this.flag=='users'){
								this.$storage.set('headportrait',this.ruleForm.image)
							}
						}
					});
				} else {
					this.$message.error(data.msg);
				}
			});
		}
	}
};
</script>
