				<el-form-item :style='{"border":"0px solid #eee","width":"49%","padding":"0","margin":"0 0 26px 0","display":"inline-block"}'   v-if="flag=='waimaiyuan'"  label="信誉情况" prop="xinyuqingkuang">
					<el-input v-model="ruleForm.xinyuqingkuang" readonly						placeholder="信誉情况" clearable></el-input>
				</el-form-item>
				<el-form-item :style='{"border":"0px solid #eee","width":"49%","padding":"0","margin":"0 0 26px 0","display":"inline-block"}'   v-if="flag=='xiaoquguanliyuan'"  label="管理账号" prop="guanlizhanghao">
					<el-input v-model="ruleForm.guanlizhanghao" readonly						placeholder="管理账号" clearable></el-input>
				</el-form-item>
				<el-form-item :style='{"border":"0px solid #eee","width":"49%","padding":"0","margin":"0 0 26px 0","display":"inline-block"}'   v-if="flag=='xiaoquguanliyuan'"  label="管理姓名" prop="guanlixingming">
