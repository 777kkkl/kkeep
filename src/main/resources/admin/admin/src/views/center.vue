				<el-form-item :style='{"border":"0px solid #eee","width":"49%","padding":"0","margin":"0 0 26px 0","display":"inline-block"}'   v-if="flag=='waimaiyuan'"  label="配送平台" prop="peisongpingtai">
					<el-input v-model="ruleForm.peisongpingtai" 						placeholder="配送平台" clearable></el-input>
